# var.py
# This file demonstrates creating and using variables in Python

# String variable
greeting = "Hello World"
print(greeting)

# Integer variable
year = 2026
print("Year:", year)

# Float variable
temperature = 36.6
print("Temperature:", temperature)

# Boolean variable
is_raining = False
print("Is it raining?", is_raining)

# Multiple assignment
x, y, z = 10, 20, 30
print("x:", x, "y:", y, "z:", z)

# Updating variable
x = x + 5
print("Updated x:", x)